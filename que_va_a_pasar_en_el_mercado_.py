def train_predict_with_explain(target_col, min_train=900):
    """
    target_col: columna de forward return (ej. 'Forward 5d: Oro (ETF GLD)')
    Devuelve probabilidad de subir hoy y explicación (top drivers).
    """
    d = df5[MODEL_FEATURES + [target_col]].dropna().copy()
    if len(d) < min_train:
        return np.nan, [], len(d)

    y = (d[target_col] > 0).astype(int).values
    X = d[MODEL_FEATURES].values

    # Entrenar con todo menos el último punto
    pipe.fit(X[:-1], y[:-1])

    # Predecir en el último punto (que corresponde a "hoy" del dataset recortado)
    x_last = X[-1:]
    p_up = float(pipe.predict_proba(x_last)[0,1])

    # Explicación: contribuciones = coef * z (en espacio estandarizado)
    scaler = pipe.named_steps["scaler"]
    lr = pipe.named_steps["lr"]

    z_last = scaler.transform(x_last)[0]
    coefs = lr.coef_[0]
    contrib = coefs * z_last

    # Top 3 drivers positivos y negativos
    idx_sorted = np.argsort(np.abs(contrib))[::-1]
    top = []
    for i in idx_sorted[:6]:
        feat = MODEL_FEATURES[i]
        sign = "↑" if contrib[i] > 0 else "↓"
        # valor actual vs mediana 5y
        cur = df5.loc[d.index[-1], feat]
        med = df5[feat].median()
        rel = "por encima de" if cur > med else "por debajo de"
        top.append((feat, sign, float(contrib[i]), float(cur) if pd.notna(cur) else None, rel, float(med) if pd.notna(med) else None))

    return p_up, top, len(d)

