def fred_try_series(series_id, start, end):
    try:
        s = pdr.DataReader(series_id, "fred", start, end)[series_id]
        return s.dropna()
    except Exception:
        return pd.Series(dtype=float)

def download_fred_candidates(candidates_dict, start, end):
    data = {}
    chosen = {}
    missing = []

    for key, (full_name, series_list) in candidates_dict.items():
        best = pd.Series(dtype=float)
        best_id = None

        for sid in series_list:
            s = fred_try_series(sid, start, end)
            if s is not None and s.dropna().shape[0] >= 10:
                best = s
                best_id = sid
                break

        if best_id is None:
            missing.append(key)
            chosen[key] = None
            data[key] = pd.Series(dtype=float)
        else:
            chosen[key] = best_id
            data[key] = best

    df = pd.DataFrame(data).sort_index()
    return df, chosen, missing

