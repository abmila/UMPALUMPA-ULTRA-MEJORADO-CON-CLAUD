def _download_one_ticker(ticker, start, end, auto_adjust=True):
    df = yf.download(
        tickers=ticker,
        start=start,
        end=end + pd.Timedelta(days=1),   # end es exclusivo en yfinance
        auto_adjust=auto_adjust,
        progress=False,
        threads=False,
        group_by="column"
    )
    if df is None or len(df) == 0:
        return pd.Series(dtype=float)
    if isinstance(df.columns, pd.MultiIndex):
        s = df["Close"][ticker].copy() if ticker in df["Close"].columns else df["Close"].iloc[:,0].copy()
    else:
        s = df["Close"].copy() if "Close" in df.columns else df.iloc[:,0].copy()
    s = s.dropna()
    s.name = ticker
    return s

def download_universe_yahoo(universe_dict, start, end, auto_adjust=True, min_points=200):
    """
    Para cada clave (ej. 'DXY'), intenta tickers en orden hasta que uno funcione.
    Retorna:
      - prices_df: columnas por clave (SPY, DXY, etc.)
      - chosen: ticker elegido por clave
      - missing: claves que no lograron bajar
    """
    prices = pd.DataFrame()
    chosen = {}
    missing = []

    for key, (full_name, tickers) in universe_dict.items():
        best = pd.Series(dtype=float)
        best_ticker = None

        for tkr in tickers:
            s = _download_one_ticker(tkr, start, end, auto_adjust=auto_adjust)
            if s is not None and s.dropna().shape[0] >= min_points:
                best = s
                best_ticker = tkr
                break

        if best_ticker is None:
            missing.append(key)
            chosen[key] = None
            prices[key] = np.nan
        else:
            chosen[key] = best_ticker
            prices[key] = best

    prices = prices.sort_index()
    # quitar última fila si es pura NaN (a veces aparece un día extra)
    if prices.tail(1).isna().all(axis=1).iloc[0]:
        prices = prices.iloc[:-1]

    return prices, chosen, missing

