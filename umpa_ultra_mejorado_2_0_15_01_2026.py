def dividend_ttm(tk: yf.Ticker, current_price: float) -> Tuple[float,float]:
    div_ttm = 0.0
    try:
        divs = tk.dividends
        if isinstance(divs, pd.Series) and not divs.empty:
            cutoff = datetime.today() - timedelta(days=365)
            div_ttm = float(divs[divs.index >= cutoff].sum())
    except Exception:
        pass
    info = tk.info or {}
    if (div_ttm == 0.0) and (info.get("dividendRate") is not None):
        try: div_ttm = float(info.get("dividendRate"))
        except Exception: pass
    yld = (div_ttm/current_price) if (current_price and current_price>0) else np.nan
    if yld and yld > 1.5: yld = yld/100.0
    return div_ttm, yld

def ddm_2stage(div_ttm_ps: float, Re: float, g_exp: float, g_term: float, years: int=YEARS_PROJ) -> float:
    if div_ttm_ps <= 0 or Re <= g_term+1e-4: return np.nan
    pv = 0.0
    for t in range(1, years+1):
        Dt = div_ttm_ps * ((1+g_exp)**t)
        pv += Dt / ((1+Re)**t)
    Dn = div_ttm_ps * ((1+g_exp)**years)
    TV = Dn*(1+g_term)/(Re-g_term)
    return float(pv + TV/((1+Re)**years))

def compute_financials_models(
    tk: yf.Ticker, price_ccy: str, valuation_ccy: str, rf: float, erp: float,
    rf_src: str, erp_src: str, alias: dict
):
    info = tk.info or {}
    fin_ccy = (info.get("financialCurrency") or valuation_ccy).upper()

    px, sh, price_ccy, shares_source, curr_check = safe_price_and_shares(tk, price_ccy)
    if np.isnan(px) or np.isnan(sh) or sh<=0:
        return np.nan, {"hard_error": "no price/shares"}, np.nan, "FIN_no_price_or_shares"

    # Beta usada para Re (consistente con industrial)
    beta_pack = beta_weekly_5y_robust(tk.ticker, price_ccy, rf)
    beta_used = float(beta_pack["beta_used"]); beta_src  = beta_pack["beta_source"]
    Re = rf + beta_used*erp

    # DDM 2-etapas si hay dividendos
    div_ttm, _ = dividend_ttm(tk, px)
    cc = valuation_ccy.upper()
    if cc in ("USD","EUR"): g_lo,g_hi = GTERM_DEV.get(cc,(0.015,0.025))
    else:                   g_lo,g_hi = GTERM_EM
    g_term = min(g_hi, Re-0.01) if Re>0.01 else g_lo
    g_exp  = min(0.06, Re-0.015)

    val_ps_val = ddm_2stage(div_ttm, Re, g_exp, g_term, YEARS_PROJ) if div_ttm>0 else np.nan

    # Fallback: Exceso de retornos si DDM no aplica
    if np.isnan(val_ps_val):
        fields = extract_time_series_v4(tk, 5, alias)
        spot = fx_spot((info.get("financialCurrency") or valuation_ccy), valuation_ccy)
        eq_last = fields.get("equity_last", np.nan)
        ni0     = first_non_nan(fields.get("net_income",[]))
        BVPS_val = (eq_last/sh)*spot if (not np.isnan(eq_last) and sh>0) else np.nan
        if np.isnan(BVPS_val) or BVPS_val<=0:
            # ✅ Fallback final a DCF industrial
            fields = extract_time_series_v4(tk, 6, alias)
            return compute_dcf_industrial(tk, fields, price_ccy, valuation_ccy, rf, erp, rf_src, erp_src)

        eq_prev = np.nan
        bal = fields.get("bal", pd.DataFrame())
        if bal is not None and not bal.empty and bal.shape[1] >= 2:
            e0,_,_ = find_value(bal, load_alias_builtin()["equity_total"], bal.columns[0])
            e1,_,_ = find_value(bal, load_alias_builtin()["equity_total"], bal.columns[1])
            eq_last = e0 if not np.isnan(e0) else eq_last
            eq_prev = e1
        ROE = np.nan
        if not np.isnan(ni0) and not np.isnan(eq_last) and eq_last>0:
            eq_avg = eq_last if np.isnan(eq_prev) else (eq_last+eq_prev)/2.0
            if eq_avg>0: ROE = float(ni0/eq_avg)
        if np.isnan(ROE): ROE = 0.12

        payout = 0.0
        try:
            if (not np.isnan(ni0)) and ni0>0 and div_ttm>0:
                payout = max(0.0, min(1.0, (div_ttm*sh)/ni0))
        except Exception:
            pass
        b = 1.0 - payout
        g_exp = float(np.clip(b*ROE, MIN_G_EXPL, MAX_G_EXPL))
        # PV de residuales + valor terminal
        residuals_pv = 0.0
        for t in range(1, YEARS_PROJ+1):
            residual_t = (ROE - Re) * (BVPS_val * (b**(t-1)))
            residuals_pv += residual_t / ((1+Re)**t)
        BVN  = BVPS_val * (b**YEARS_PROJ)
        term = ((ROE - Re) * BVN) * (1+g_term) / (((1+Re)**YEARS_PROJ) * (Re - g_term)) if Re>g_term+1e-4 else 0.0
        val_ps_val = BVPS_val + residuals_pv + term

    # ✅ Si aún no hay valor, fallback a DCF industrial
    if np.isnan(val_ps_val):
        fields = extract_time_series_v4(tk, 6, alias)
        return compute_dcf_industrial(tk, fields, price_ccy, valuation_ccy, rf, erp, rf_src, erp_src)

    intrinsic_px_price_ccy = val_ps_val * (fx_spot(valuation_ccy, price_ccy) if valuation_ccy!=price_ccy else 1.0)
    meta = dict(
        fin_ccy=fin_ccy, price_ccy=price_ccy, valuation_ccy=valuation_ccy,
        rf=rf, erp=erp, rf_source=rf_src, erp_source=erp_src,
        Re=Re, Rd=np.nan, Rd_eff=np.nan, tax_rate=np.nan, WACC=Re,
        beta_calc=beta_pack["beta_calc"], beta_info=beta_pack["beta_info"], beta_used=float(beta_used),
        beta_R2=beta_pack["R2"], beta_N=beta_pack["N"], beta_source=beta_src,
        g_exp=g_exp, g_term=g_term, FCFF0=np.nan, E=np.nan, D_gross=np.nan, Cash=np.nan,
        DATA_GAPS=False, SHARES_SOURCE=shares_source, CURRENCY_CHECK=curr_check,
        assumptions="Financials: DDM/Excess-Returns; FX spot; β semanal robusta con fallback a DCF industrial"
    )
    return intrinsic_px_price_ccy, meta, px, "FIN_OK"

