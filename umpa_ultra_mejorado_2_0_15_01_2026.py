def _parse_tickers(raw: str):
    arr = [sanitize_symbol(t) for t in raw.split(",") if str(t).strip()]
    out=[CANONICAL_TICKERS.get(t, t) for t in arr]
    known_suffixes={'MX','TO','SA','L','PA','HK','SW','SS','SZ','AX','F','DE','MI','AS','VI','HE','CO','JK','NZ','TW'}
    out=[t.replace(".", "-") if "." in t and t.split(".")[-1] not in known_suffixes else t for t in out]
    return list(dict.fromkeys(out))

def is_etf_or_index(sym: str, info: dict) -> bool:
    qt = str((info or {}).get("quoteType") or "").upper()
    if qt in {"ETF","INDEX","MUTUALFUND","FUND"}: return True
    if sym.startswith("^"): return True
    ln = str((info or {}).get("longName") or ""); sn = str((info or {}).get("shortName") or "")
    return ("ETF" in ln.upper()) or ("ETF" in sn.upper())

def align_mu_sigma(val_df: pd.DataFrame, cov_ann: pd.DataFrame, ordered_tickers: List[str], mu_col="MU_ADJ") -> Tuple[np.ndarray,np.ndarray,List[str]]:
    v = val_df.set_index("Ticker")
    v = v[np.isfinite(v[mu_col])].copy()
    inter = [t for t in ordered_tickers if t in v.index and t in cov_ann.index]
    if len(inter)<2:
        raise RuntimeError("No hay suficientes tickers comunes entre valuación y covarianza.")
    v = v.loc[inter]
    mu = v[mu_col].astype(float).values
    Sigma = cov_ann.loc[inter, inter].values
    # Ridge mínimo para PSD
    try: np.linalg.cholesky(Sigma)
    except np.linalg.LinAlgError: Sigma = Sigma + np.eye(Sigma.shape[0])*1e-8
    return mu, Sigma, inter

def max_sharpe_with_sigma_cap(mu: np.ndarray, Sigma: np.ndarray, rf: float, sigma_cap: float, bounds=None) -> np.ndarray:
    n=len(mu)
    bounds = bounds or [(0.0,1.0)]*n
    cons=[{'type':'eq','fun': lambda w: np.sum(w)-1.0}]
    if sigma_cap and sigma_cap>0:
        cons.append({'type':'ineq','fun': lambda w: sigma_cap - np.sqrt(w @ Sigma @ w)})
    w0=np.array([ (low+high)/2 for (low,high) in bounds ], dtype=float)
    def neg_sharpe(w):
        ret=float(w @ mu); vol=float(np.sqrt(w @ Sigma @ w))
        return -((ret-rf)/vol) if vol>0 else 1e9
    res=minimize(neg_sharpe, w0, method="SLSQP", bounds=bounds, constraints=cons, options=dict(maxiter=600))
    w=np.clip(res.x if res.success else w0, 0, 1); s=w.sum()
    return w/s if s>0 else np.ones(n)/n

