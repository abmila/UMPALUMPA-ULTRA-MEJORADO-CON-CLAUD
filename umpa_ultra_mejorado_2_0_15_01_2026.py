def _norm(s: str) -> str:
    s = unicodedata.normalize("NFKD", str(s)).encode("ascii","ignore").decode("ascii")
    s = s.lower().replace("&", " and ")
    s = re.sub(r"[^\w\s]", " ", s)
    return re.sub(r"\s+", " ", s).strip()

def fx_pair(a: str, b: str) -> str:
    return f"{a}{b}=X"

def fx_spot(from_ccy: str, to_ccy: str) -> float:
    if not from_ccy or not to_ccy or from_ccy == to_ccy: return 1.0
    try:
        px = yf.Ticker(fx_pair(from_ccy,to_ccy)).history(period="5d", interval="1d")["Close"].dropna()
        return float(px.iloc[-1]) if not px.empty else 1.0
    except Exception:
        return 1.0

def get_fx_series(from_ccy: str, to_ccy: str, start: str) -> pd.Series:
    if not from_ccy or not to_ccy or from_ccy == to_ccy:
        return pd.Series(1.0, index=pd.date_range(start=start, end=datetime.today(), freq="D"))
    for pair, invert in [(fx_pair(from_ccy,to_ccy), False),(fx_pair(to_ccy,from_ccy), True)]:
        try:
            s = yf.Ticker(pair).history(start=start, auto_adjust=False)["Close"].dropna()
            if not s.empty:
                return (1.0/s) if invert else s
        except Exception:
            pass
    return pd.Series(1.0, index=pd.date_range(start=start, end=datetime.today(), freq="D"))

