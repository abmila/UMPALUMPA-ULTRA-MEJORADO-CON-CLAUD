CCY_NORMALIZATION = {"GBp": ("GBP", 0.01), "ZAc": ("ZAR", 0.01)}

def normalize_price_currency(info_currency: str, series: Optional[pd.Series]=None, last_price: Optional[float]=None):
    ccy = (info_currency or "").strip()
    if ccy in CCY_NORMALIZATION:
        ccy_new, factor = CCY_NORMALIZATION[ccy]
        if series is not None: series = series * factor
        if last_price is not None: last_price = last_price * factor
        return ccy_new, series, last_price
    return ccy, series, last_price

def sanitize_symbol(sym: str) -> str:
    if not sym: return sym
    s = str(sym).strip().upper().lstrip("$")
