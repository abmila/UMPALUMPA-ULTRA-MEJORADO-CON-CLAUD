# Celda 5: FCFF ROBUSTO + FADE g_exp→g_term
# ==============================================
def robust_fcff0(fields: dict, etr_final: float) -> Tuple[float, dict]:
    """
    FCFF0 = NOPAT0 + D&A_avg - Capex_avg - ΔNWC_avg
    Todos los promedios por defecto en 3 años (si hay 5, permitir 5; si no, TTM_4Q).
    Retorna FCFF0 (en moneda de estados) y dict con promedios y método.
    """
    # series
    ebit    = fields.get("ebit",[]) or []
    da_arr  = fields.get("da",[]) or []
    capex   = fields.get("capex",[]) or []
    dNWC    = fields.get("delta_nwc",[]) or []

    def _clean_numeric(a):
        return [float(x) for x in a if x is not None and np.isfinite(x)]

    da_c   = _clean_numeric(da_arr)
    cap_c  = _clean_numeric(capex)
    dnwc_c = _clean_numeric(dNWC)

    # Elegimos la ventana más robusta disponible: 5 → 3 → 1/TTM
    method = "ANNUAL_3Y_MEAN"
    def _avg_last(a, n):
        return float(np.mean(a[:n])) if len(a)>=n else (float(np.mean(a)) if len(a)>=1 else np.nan)

    # Por diseño priorizo 5 si está disponible
    if len(da_c)>=5 and len(cap_c)>=5 and len(dnwc_c)>=5:
        DA_avg = _avg_last(da_c,5); CAPEX_avg = _avg_last(cap_c,5); DNWC_avg = _avg_last(dnwc_c,5)
        method = "ANNUAL_5Y_MEAN"
    elif len(da_c)>=3 and len(cap_c)>=3 and len(dnwc_c)>=3:
        DA_avg = _avg_last(da_c,3); CAPEX_avg = _avg_last(cap_c,3); DNWC_avg = _avg_last(dnwc_c,3)
        method = "ANNUAL_3Y_MEAN"
    else:
        # Intento TTM simple (ya hicimos fallback arriba al construir ΔNWC)
        DA_avg = _avg_last(da_c,1)
        CAPEX_avg = _avg_last(cap_c,1)
        DNWC_avg  = _avg_last(dnwc_c,1)
        method = "TTM_4Q"  # etiqueta

    ebit0 = first_non_nan(ebit, np.nan)
    if np.isnan(ebit0):
        return np.nan, {"DATA_GAPS": True, "why": "missing EBIT", "ΔNWC_METHOD": method}

    NOPAT0 = float(ebit0) * (1 - float(etr_final))
    for_check = [DA_avg, CAPEX_avg, DNWC_avg]
    if any([np.isnan(x) for x in for_check]):
        return np.nan, {"DATA_GAPS": True, "why": "missing DA/Capex/ΔNWC", "ΔNWC_METHOD": method}

    FCFF0 = float(NOPAT0 + DA_avg - CAPEX_avg - DNWC_avg)
    meta = {"DELTA_NWC_AVG": DNWC_avg, "CAPEX_AVG": CAPEX_avg, "DA_AVG": DA_avg,
            "NOPAT0": NOPAT0, "ΔNWC_METHOD": method, "DATA_GAPS": False}
    return FCFF0, meta

def compute_roic_and_gexp(ebit0, tax_r, ppe_last, operating_nwc_last, reinvestment):
    # CIO ≈ NetPPE + NWC operativo
    CIO0 = float(max(1e-6, (0.0 if np.isnan(ppe_last) else ppe_last) + (0.0 if np.isnan(operating_nwc_last) else operating_nwc_last)))
    NOPAT0 = float(ebit0) * (1 - float(tax_r))
    ROIC0 = float(NOPAT0 / CIO0) if CIO0>0 else np.nan
    b0 = max(0.0, float(reinvestment) / max(1e-6, NOPAT0)) if NOPAT0>0 else 0.0
    g_exp = float(np.clip(b0 * (ROIC0 if not np.isnan(ROIC0) else 0.1), MIN_G_EXPL, MAX_G_EXPL))
    return NOPAT0, ROIC0, b0, g_exp, CIO0

def project_fcff_with_fade(fcff0: float, g_exp: float, g_term: float, h: int, method: str="linear") -> List[float]:
    if method.lower() == "linear":
        gs = [g_exp - (g_exp - g_term) * (t/h) for t in range(1, h+1)]
        fcffs = []
        last = fcff0
        for g in gs:
            last = last*(1+g)
            fcffs.append(last)
        return fcffs
    # H-Model (dividend-intuition adaptada a FCFF)
    H = h/2.0
    # Para H-Model tradicional se usaría una fórmula cerrada en DDM.
    # Aquí devolvemos la senda lineal como aproximación estable si no hay driver adicional.
    gs = [g_exp - (g_exp - g_term) * (t/h) for t in range(1, h+1)]
    fcffs = []
    last = fcff0
    for g in gs:
        last = last*(1+g)
        fcffs.append(last)
    return fcffs

