def _ensure_db_exists():
    if not os.path.exists(PORTFOLIO_DB_FILE):
        with open(PORTFOLIO_DB_FILE, "w", encoding="utf-8") as f:
            json.dump([], f)

def list_portfolios():
    try:
        _ensure_db_exists()
        lst = json.load(open(PORTFOLIO_DB_FILE, "r", encoding="utf-8"))
        out=[]
        for i,rec in enumerate(lst):
            out.append((i, rec.get("name",""), rec.get("timestamp",""), rec.get("universe",[])))
        return out
    except Exception as e:
        log.warning(f"No se pudo leer {PORTFOLIO_DB_FILE}: {e}")
        return []

def save_portfolio(name: str, universe: List[str], weights: Dict[str,float], qty: Dict[str,int], base_ccy: str):
    try:
        _ensure_db_exists()
        lst = json.load(open(PORTFOLIO_DB_FILE, "r", encoding="utf-8"))
        rec = {
            "name": name, "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            "universe": list(universe),
            "weights": {k: float(weights.get(k,0.0)) for k in universe},
            "qty": {k: int(qty.get(k,0)) for k in universe},
            "valuation_ccy": base_ccy
        }
        lst.insert(0, rec); lst = lst[:5]
        json.dump(lst, open(PORTFOLIO_DB_FILE, "w", encoding="utf-8"), ensure_ascii=False, indent=2)
        return True
    except Exception as e:
        log.warning(f"save_portfolio error: {e}")
        return False

