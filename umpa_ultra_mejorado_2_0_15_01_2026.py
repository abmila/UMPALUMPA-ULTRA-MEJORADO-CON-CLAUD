            try:
                alpha_dcf = (intrinsic/market_px) - 1.0 if (intrinsic and market_px and market_px>0) else np.nan
            except Exception:
                pass

            # === Cálculos para columnas nuevas (ya existentes en tu 8.1) ===
            fin_ccy = meta.get("fin_ccy", valuation_ccy)

