def safe_price_and_shares(tk: yf.Ticker, price_ccy_hint: str):
    """
    Devuelve: px, sh, price_ccy, shares_source(str), currency_check(str: OK/REQUIRED)
    Marca MKTCAP/PX cuando se infiere shares; si eso pasa ⇒ CURRENCY_CHECK = REQUIRED
    """
    info = tk.info or {}
    # Precio
    px = None
    if hasattr(tk,"fast_info") and tk.fast_info.get("lastPrice") is not None:
        px = float(tk.fast_info["lastPrice"])
    else:
        rawp = info.get("currentPrice") or info.get("previousClose")
        if rawp is not None:
            px = float(rawp)
    if px is None or (isinstance(px,float) and np.isnan(px)):
        try:
            h = tk.history(period="5d")["Close"].dropna()
            if len(h)>0: px = float(h.iloc[-1])
        except Exception:
            pass
    price_ccy = (info.get("currency") or price_ccy_hint or "USD").upper()
    price_ccy, _, px = normalize_price_currency(price_ccy, None, px)

    # Acciones
    shares_source = "INFO.sharesOutstanding"
    sh = info.get("sharesOutstanding") or info.get("floatShares") or info.get("impliedSharesOutstanding")
    if (sh is None or (isinstance(sh,(float,int)) and (np.isnan(sh) or sh<=0))) and hasattr(tk,"get_shares"):
        try:
            gs = tk.get_shares()
            if isinstance(gs, pd.DataFrame) and not gs.empty:
                if "Diluted" in gs:
                    sh = float(gs["Diluted"].dropna().iloc[-1]); shares_source = "get_shares.Diluted"
                elif "Basic" in gs:
                    sh = float(gs["Basic"].dropna().iloc[-1]);   shares_source = "get_shares.Basic"
        except Exception:
            pass
    if (sh is None or (isinstance(sh,float) and (np.isnan(sh) or sh<=0))) and (info.get("marketCap") and px and px>0):
        sh = float(info["marketCap"])/float(px)
        shares_source = "MKTCAP/PX"
    if sh is None or (isinstance(sh,float) and (np.isnan(sh) or sh<=0)) or px is None or (isinstance(px,float) and np.isnan(px)):
        return np.nan, np.nan, price_ccy, "MISSING", "REQUIRED"
    currency_check = "REQUIRED" if shares_source=="MKTCAP/PX" else "OK"
    return float(px), float(sh), price_ccy, shares_source, currency_check

def compute_gross_debt(fields: dict) -> float:
    """
    Deuda bruta = current debt + long-term debt + lease liabilities (cuando no hay total_debt).
    Usamos el último año disponible (ya compuesto en extract_time_series_v4).
    """
    debt_last = fields.get("debt_total_last", np.nan)
    return 0.0 if np.isnan(debt_last) else float(debt_last)

def compute_dcf_industrial(
    tk: yf.Ticker,
    fields: dict,
    price_ccy: str,
    valuation_ccy: str,
    rf: float, erp: float,
    rf_src: str, erp_src: str,
    fade_h: int = YEARS_PROJ,
    fade_method: str = CFG["FADE_METHOD"]
):
    info = tk.info or {}
    fin_ccy = (info.get("financialCurrency") or valuation_ccy).upper()
    country = (info.get("country") or "").strip() or "_DEFAULT"

    # Precio y acciones (con fuentes)
    px, sh, price_ccy, shares_source, curr_check = safe_price_and_shares(tk, price_ccy)
    if np.isnan(px) or np.isnan(sh) or sh<=0:
        return np.nan, {"hard_error": "no price/shares"}, np.nan, "no_price_or_shares"

    # ETR anual mediana 3y con cap país
    etr_final, etr_med, etr_source = compute_annual_etr(
        fields.get("tax_exp",[]), fields.get("ebit",[]), fields.get("interest_exp",[]), country
    )

    # FCFF0 robusto (EN moneda de estados)
    FCFF0_fin, fcff_meta = robust_fcff0(fields, etr_final)

    # FX spot a moneda de valoración
    spot = fx_spot(fin_ccy, valuation_ccy)
    FCFF0_val = FCFF0_fin * spot if np.isfinite(FCFF0_fin) else np.nan

    # Beta robusta semanal 5y (regla determinística)
    beta_pack = beta_weekly_5y_robust(tk.ticker, price_ccy, rf)
    beta_used = float(beta_pack["beta_used"])
    beta_src  = beta_pack["beta_source"]

    # Costos de capital
    Re = float(rf + beta_used*erp)

    # g_term por macro y moneda
    cc = valuation_ccy.upper()
    if cc in ("USD","EUR"): g_lo,g_hi = GTERM_DEV.get(cc,(0.015,0.025))
    else:                   g_lo,g_hi = GTERM_EM
    g_term = min(g_hi, Re - 0.01) if Re>0.01 else g_lo

    # g_exp vía ROIC/reinversión (opcionalmente, si tenemos ppe y NWC operativo)
    ebit0 = first_non_nan(fields.get("ebit",[]), np.nan)
    operating_nwc_last = np.nan
    try:
        bal = fields.get("bal", pd.DataFrame()); alias = load_alias_builtin()
        if bal is not None and not bal.empty:
            NWC, _, _ = _compute_operating_nwc_arrays(bal, alias)
            if len(NWC)>0 and not np.isnan(NWC[0]): operating_nwc_last = float(NWC[0])
    except Exception:
        pass
    capex0 = first_non_nan(fields.get("capex",[]), 0.0)
    da0    = first_non_nan(fields.get("da",[]), 0.0)
    dNWC0  = first_non_nan(fields.get("delta_nwc",[]), 0.0)
    reinv_fin = (0.0 if np.isnan(capex0) else capex0) - (0.0 if np.isnan(da0) else da0) + (0.0 if np.isnan(dNWC0) else dNWC0)

    _, ROIC0, b0, g_exp_raw, CIO0 = compute_roic_and_gexp(
        ebit0, etr_final, fields.get("ppe_last", np.nan), operating_nwc_last, reinv_fin
    )
    g_exp = float(np.clip(g_exp_raw, MIN_G_EXPL, MAX_G_EXPL))

    # Proyección con fade
    if not np.isfinite(FCFF0_val):
        return np.nan, {"hard_error": "no FCFF0"}, px, "GateA_fail"
    fcffs = project_fcff_with_fade(FCFF0_val, g_exp, g_term, fade_h, method=fade_method)

    # Estructura de capital observada (deuda BRUTA en los PESOS del WACC)
    px_val = px * fx_spot(price_ccy, valuation_ccy)
    E_mkt  = max(px_val * sh, 0.0)

    D_gross = compute_gross_debt(fields) * spot    # deuda bruta en moneda de valoración
    Cash_c  = (0.0 if np.isnan(fields.get("cash_last", np.nan)) else float(fields.get("cash_last"))) * spot

    # Rd efectivo aproximado: interés/ deuda promedio (deuda bruta)
    debt_last = fields.get("debt_total_last", np.nan)
    debt_prev = fields.get("debt_total_prev", np.nan)
    int0      = first_non_nan(fields.get("interest_exp",[]), np.nan)
    if np.isnan(int0) or (np.isnan(debt_last) and np.isnan(debt_prev)) or (max(1e-6, (0.0 if np.isnan(debt_last) else debt_last) + (0.0 if np.isnan(debt_prev) else debt_prev))<=0):
        Rd = max(rf, 0.05); kd_src="Rd_fallback"
    else:
        avg_debt = 0.5*((0.0 if np.isnan(debt_last) else float(debt_last)) + (0.0 if np.isnan(debt_prev) else float(debt_prev)))
        Rd = float(np.clip(abs(float(int0)) / max(1e-6, avg_debt), rf, 0.25))
        kd_src="interest/avgDebt"
    Rd_eff = Rd*(1.0 - etr_final)

    V = float(E_mkt + D_gross)
    if V>0:
        wE = E_mkt / V; wD = D_gross / V
        WACC = wE*Re + wD*Rd_eff
    else:
        WACC = Re

    # Validación g_term < WACC (si no, recortar g_term)
    if WACC <= g_term + 1e-4:
        g_term = max(min(g_term, WACC-0.01), -0.20)

    # Valuación: PV de explícitos + TV Gordon en t=h
    PV = sum(fc/((1+WACC)**i) for i,fc in enumerate(fcffs,1))
    TV = fcffs[-1]*(1+g_term)/(WACC-g_term)
    EV = PV + TV/((1+WACC)**fade_h)

    # Puente EV → Equity (ahora sí usa Net Debt)
    net_debt = D_gross - Cash_c
    equity_value = EV - net_debt
    intrinsic_val_ps_valccy = equity_value / sh
    intrinsic_px_price_ccy = intrinsic_val_ps_valccy * (fx_spot(valuation_ccy, price_ccy) if valuation_ccy!=price_ccy else 1.0)

    meta = dict(
        fin_ccy=fin_ccy, price_ccy=price_ccy, valuation_ccy=valuation_ccy,
        rf=rf, erp=erp, rf_source=rf_src, erp_source=erp_src,
        beta_calc=beta_pack["beta_calc"], beta_info=beta_pack["beta_info"], beta_used=beta_used,
        beta_R2=beta_pack["R2"], beta_N=beta_pack["N"], beta_source=beta_src,
        Re=Re, Rd=Rd, Rd_eff=Rd_eff, tax_rate=etr_final, ETR_3Y_MEDIAN=etr_med, ETR_SOURCE=etr_source,
        WACC=WACC, WACC_NET_DEBT=False, g_exp=g_exp, g_term=g_term, FCFF0=FCFF0_val,
        E=E_mkt, D_gross=D_gross, Cash=Cash_c,
        DATA_GAPS=bool(fcff_meta.get("DATA_GAPS", False)),
        DELTA_NWC_AVG=fcff_meta.get("DELTA_NWC_AVG", np.nan),
        CAPEX_AVG=fcff_meta.get("CAPEX_AVG", np.nan),
        DA_AVG=fcff_meta.get("DA_AVG", np.nan),
        ΔNWC_METHOD=fcff_meta.get("ΔNWC_METHOD",""),
        SHARES_SOURCE=shares_source, CURRENCY_CHECK=curr_check,
        assumptions=f"Fade={fade_method} h={fade_h}; WACC pesos con DEUDA BRUTA; EV→Equity con NET DEBT"
    )
    return intrinsic_px_price_ccy, meta, px, "DCF_Industrial_OK"

